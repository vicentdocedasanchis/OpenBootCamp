public class Ejercicio_T3 {
    public static void main(String[] args) {
        String[] palabras = {"Hola", "mundo", "desde", "Java"};
        String resultado = "";

        for (int i = 0; i < palabras.length; i++) {
            resultado += palabras[i] + " ";
        }
        System.out.println(resultado);

    }




}
